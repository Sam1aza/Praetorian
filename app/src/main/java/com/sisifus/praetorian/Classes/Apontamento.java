package com.sisifus.praetorian.Classes;

public class Apontamento {

    private String glicemia;
    private String pressaoArterial;
    private String dataMedicao;
    private String keyApontamento;
    private String batimento;
    private String horaApontamento;
    private String UsuarioApontamento;
    private String Observacao;
    private String checkMedicamento;

    public String getGlicemia() {
        return glicemia;
    }

    public void setGlicemia(String glicemia) {
        this.glicemia = glicemia;
    }

    public String getPressaoArterial() {
        return pressaoArterial;
    }

    public void setPressaoArterial(String pressaoArterial) {
        this.pressaoArterial = pressaoArterial;
    }

    public String getDataMedicao() {
        return dataMedicao;
    }

    public void setDataMedicao(String dataMedicao) {
        this.dataMedicao = dataMedicao;
    }

    public String getKeyApontamento() {
        return keyApontamento;
    }

    public void setKeyApontamento(String keyApontamento) {
        this.keyApontamento = keyApontamento;
    }

    public String getBatimento() {
        return batimento;
    }

    public void setBatimento(String batimento) {
        this.batimento = batimento;
    }

    public String getHoraApontamento() {
        return horaApontamento;
    }

    public void setHoraApontamento(String horaApontamento) {
        this.horaApontamento = horaApontamento;
    }

    public String getUsuarioApontamento() {
        return UsuarioApontamento;
    }

    public void setUsuarioApontamento(String usuarioApontamento) {
        UsuarioApontamento = usuarioApontamento;
    }

    public String getObservacao() {
        return Observacao;
    }

    public void setObservacao(String observacao) {
        Observacao = observacao;
    }

    public String getCheckMedicamento() {
        return checkMedicamento;
    }

    public void setCheckMedicamento(String checkMedicamento) {
        this.checkMedicamento = checkMedicamento;
    }
}
